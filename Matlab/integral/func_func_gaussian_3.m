%{
f = @(x)(x.*sin(x))
%}

function [NIntF, IntF, IntErr] = func_func_gaussian_3(f)

% Algorithm
NIntF = (5*f(-sqrt(3/5)) + 8*f(0) + 5*f(sqrt(3/5)))/9;

% Compare
IntF = integral(f, -1, 1);
IntErr = abs(NIntF - IntF);

