%{
x = [0, 0.25, 0.5, 0.75, 1];
y = [0, 0.47314, 0.90546, 1.30962, 1.69375];
%}

function [cba] = func_tbl_quadratic_least_squares(x, y)

% Algorithm
y = y';
n = length(y);

A = [n, sum(x), sum(x.^2);
    sum(x), sum(x.^2), sum(x.^3);
    sum(x.^2), sum(x.^3), sum(x.^4)];
B = [sum(y); x*y; x.^2*y];

cba = A\B;

