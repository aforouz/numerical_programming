%{
x = [0, 0.25, 0.5, 0.75, 1];
F = @(x)(x.*sin(x));
%}

function [cba] = func_func_quadratic_least_squares(x, F)

% Algorithm
y = F(x');
n = length(y);

A = [n, sum(x), sum(x.^2);
    sum(x), sum(x.^2), sum(x.^3);
    sum(x.^2), sum(x.^3), sum(x.^4)];
B = [sum(y); x*y; x.^2*y];

cba = A\B;

