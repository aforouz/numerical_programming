%{
x = [0, 0.25, 0.5, 0.75, 1];
F = @(x)(x.*sin(x));
%}

function [ba] = func_func_linear_least_squares(x, F)

% Algorithm
y = F(x');
n = length(y);

A = [n, sum(x); sum(x), sum(x.^2)];
B = [sum(y); x*y];

ba = A\B;

