clc;
clear;
close all;

f = @(x, y)(y + x^2);
x0 = 0;
y0 = 1;
h = 0.2;
xN = 1;

N = round((xN-x0)/h)+1;
x = x0:h:xN;
y = x;
y(1) = y0;

fprintf("n\tx\t\t\t\tK1\t\t\t\tK2\t\t\t\ty\n");
fprintf("0)\t%.10f\t%.10f\t%.10f\t%.10f\n", x(1), 0, 0, y(1));

for n = 1:N-1
    K1 = h*f(x(n), y(n));
    K2 = h*f(x(n) + h, y(n) + K1);
    y(n+1) = y(n) + (K1 + K2)/2;
    fprintf("%d)\t%.10f\t%.10f\t%.10f\t%.10f\n", n, x(n+1), K1, K2, y(n+1));
end

