function [y] = gauss_seidel_sys(A, b, x, e, mi)
	y = [];
    L = tril(A);
    Tg = -L\triu(A, 1);
    cg = L\b;
    for k = 1:mi
        y = Tg*x + cg;
        if norm(x-y) < e
            break
        end
        x = y;
    end
return

