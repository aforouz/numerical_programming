x = 0.34;
xi = [0, 0.25, 0.5, 0.75, 1];
f = [0, 0.47314, 0.90546, 1.30962, 1.69375];

if size(xi, 2)==1
    xi = xi';
end
if size(f, 1)==1
    f = f';
end

n = length(f);
L = ones(1, n);
for i = 1:n
    for j = 1:n
        if i ~= j
            L(i) = L(i)*(x - xi(j))/(xi(i) - xi(j));
        end
    end
end

fprintf("x\t\t\t\t|L\t\t\t\tf\n");
for i = 1:n
    fprintf("%.10f\t|%.10f\t%.10f\n", xi(i), L(i), f(i));
end

fprintf("\np(x) = %.10f\n", L*f);

