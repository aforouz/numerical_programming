function [y] = jacobi_sys(A, b, x, e, mi)
	y = [];
    D = diag(diag(A));
    Tj = -D\(triu(A, 1) + tril(A, -1));
    cj = D\b;
    for k = 1:mi
        y = Tj*x + cj;
        if norm(x-y) < e
            break
        end
        x = y;
    end
return

